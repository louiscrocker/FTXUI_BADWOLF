bool CodeGenResult::WriteToFile(const std::string& path) const {
  std::ofstream f(path);
  if (!f.is_open()) {
    return false;
  }
  f << code;
  return f.good();
}

bool CodeGenResult::LooksValid() const {
  if (code.empty()) {
    return false;
  }
  // Must reference ftxui in some way
  bool has_ftxui = code.find("ftxui") != std::string::npos;
  // Must have a main function
  bool has_main = code.find("main(") != std::string::npos ||
                  code.find("main ()") != std::string::npos;
  return has_ftxui && has_main;
}

