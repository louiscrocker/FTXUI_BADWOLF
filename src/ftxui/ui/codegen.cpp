// Simple synchronous HTTP call to Ollama /api/generate
std::string CodeGenerator::QueryOllama(const std::string& system_prompt,
                                       const std::string& user_prompt) {
#ifdef _WIN32
  WSADATA wsa;
  WSAStartup(MAKEWORD(2, 2), &wsa);
#endif

  // Connect to localhost:11434
  struct addrinfo hints{}, *res = nullptr;
  hints.ai_family = AF_INET;
  hints.ai_socktype = SOCK_STREAM;
  if (getaddrinfo("127.0.0.1", "11434", &hints, &res) != 0 || res == nullptr) {
    return {};
  }

  int sock = static_cast<int>(socket(res->ai_family, res->ai_socktype, 0));
  if (sock < 0) {
    freeaddrinfo(res);
    return {};
  }

  if (connect(sock, res->ai_addr, res->ai_addrlen) != 0) {
    freeaddrinfo(res);
#ifdef _WIN32
    closesocket(sock);
#else
    close(sock);
#endif
    return {};
  }
  freeaddrinfo(res);

  // Escape strings for JSON
  auto json_escape = [](const std::string& s) -> std::string {
    std::string out;
    out.reserve(s.size());
    for (char c : s) {
      if (c == '"') {
        out += "\\\"";
      } else if (c == '\\') {
        out += "\\\\";
      } else if (c == '\n') {
        out += "\\n";
      } else if (c == '\r') {
        out += "\\r";
      } else if (c == '\t') {
        out += "\\t";
      } else {
        out += c;
      }
    }
    return out;
  };

  // Build JSON body (non-streaming so we get one response)
  std::string body = "{\"model\":\"" + json_escape(model_) +
                     "\","
                     "\"prompt\":\"" +
                     json_escape(system_prompt + "\n\n" + user_prompt) +
                     "\","
                     "\"stream\":false}";

  std::string request =
      "POST /api/generate HTTP/1.0\r\n"
      "Host: 127.0.0.1:11434\r\n"
      "Content-Type: application/json\r\n"
      "Content-Length: " +
      std::to_string(body.size()) +
      "\r\n"
      "\r\n" +
      body;

  // Send
  std::size_t sent = 0;
  while (sent < request.size()) {
    int n = static_cast<int>(send(sock, request.data() + sent,
                                  static_cast<int>(request.size() - sent), 0));
    if (n <= 0) {
      break;
    }
    sent += static_cast<std::size_t>(n);
  }

  // Receive full response (up to 512 KB)
  std::string response;
  response.reserve(65536);
  char buf[4096];
  while (true) {
    int n = static_cast<int>(recv(sock, buf, sizeof(buf), 0));
    if (n <= 0) {
      break;
    }
    response.append(buf, static_cast<std::size_t>(n));
    if (response.size() > 512 * 1024) {
      break;
    }
  }

#ifdef _WIN32
  closesocket(sock);
  WSACleanup();
#else
  close(sock);
#endif

  // Extract the "response" field from the JSON
  auto find_str_field = [&](const std::string& key) -> std::string {
    std::string pattern = "\"" + key + "\":\"";
    auto pos = response.find(pattern);
    if (pos == std::string::npos) {
      return {};
    }
    pos += pattern.size();
    std::string out;
    while (pos < response.size()) {
      char c = response[pos++];
      if (c == '\\' && pos < response.size()) {
        char nc = response[pos++];
        if (nc == 'n') {
          out += '\n';
        } else if (nc == 't') {
          out += '\t';
        } else if (nc == '"') {
          out += '"';
        } else if (nc == '\\') {
          out += '\\';
        } else {
          out += '\\';
          out += nc;
        }
      } else if (c == '"') {
        break;
      } else {
        out += c;
      }
    }
    return out;
  };

  return find_str_field("response");
}

void CodeGenerator::ExtractCode(const std::string& llm_response,
                                CodeGenResult& result) {
  // Extract ```cpp ... ``` block
  auto extract_block = [&](const std::string& fence) -> std::string {
    auto start = llm_response.find(fence);
    if (start == std::string::npos) {
      return {};
    }
    start += fence.size();
    // Skip to end of line
    auto nl = llm_response.find('\n', start);
    if (nl == std::string::npos) {
      return {};
    }
    start = nl + 1;
    auto end = llm_response.find("```", start);
    if (end == std::string::npos) {
      return llm_response.substr(start);
    }
    return llm_response.substr(start, end - start);
  };

  std::string cpp_code = extract_block("```cpp");
  if (cpp_code.empty()) {
    cpp_code = extract_block("```c++");
  }
  if (cpp_code.empty()) {
    cpp_code = extract_block("```");
  }

  if (!cpp_code.empty()) {
    result.code = cpp_code;
  } else {
    // Fallback: use whole response as code if it looks like C++
    if (llm_response.find("#include") != std::string::npos) {
      result.code = llm_response;
    }
  }

  // Extract CMake snippet
  std::string cmake = extract_block("```cmake");
  if (cmake.empty()) {
    cmake = extract_block("```CMake");
  }
  if (!cmake.empty()) {
    result.cmake_snippet = cmake;
  }
}

// Build default variable substitutions for a request
static std::map<std::string, std::string> DefaultVars(
    const CodeGenRequest& req) {
  // Extract "app name": capitalize first word of description
  std::string app_name;
  {
    std::istringstream ss(req.description);
    std::string word;
    // Skip common stop words
    static const std::set<std::string> stop = {
        "a",  "an",  "the", "for", "with",        "using", "in",      "of",
        "to", "and", "or",  "app", "application", "ui",    "terminal"};
    while (ss >> word) {
      std::string lw = word;
      std::transform(lw.begin(), lw.end(), lw.begin(), ::tolower);
      if (stop.find(lw) == stop.end()) {
        app_name = word;
        if (!app_name.empty()) {
          app_name[0] = static_cast<char>(
              ::toupper(static_cast<unsigned char>(app_name[0])));
        }
        break;
      }
    }
    if (app_name.empty()) {
      app_name = "MyApp";
    }
  }

  // Current date string
  std::time_t t = std::time(nullptr);
  char date_buf[32];
  std::strftime(date_buf, sizeof(date_buf), "%Y-%m-%d", std::localtime(&t));

  return {
      {"APP_NAME", app_name},
      {"TIMESTAMP", std::string(date_buf)},
      {"DESCRIPTION", req.description},
  };
}

CodeGenResult CodeGenerator::GenerateFromTemplate(const CodeGenRequest& req) {
  auto t0 = std::chrono::steady_clock::now();
  CodeGenResult result;
  result.model_used = "template";

  const CodeTemplate* tmpl = TemplateLibrary::FindBest(req.description);
  if (!tmpl) {
    // Default to dashboard when nothing matches
    const auto& templates = TemplateLibrary::BuiltinTemplates();
    if (!templates.empty()) {
      tmpl = &templates[0];
    }
  }

  if (!tmpl) {
    result.error = "No templates available";
    return result;
  }

  auto vars = DefaultVars(req);
  result.code = TemplateLibrary::Fill(*tmpl, vars);
  result.cmake_snippet = TemplateLibrary::Fill(
      CodeTemplate{"", "", {}, tmpl->cmake_snippet, ""}, vars);
  result.description = "Generated from template: " + tmpl->name;
  result.success = true;

  auto t1 = std::chrono::steady_clock::now();
  result.elapsed =
      std::chrono::duration_cast<std::chrono::milliseconds>(t1 - t0);
  return result;
}

CodeGenResult CodeGenerator::Generate(const CodeGenRequest& req) {
  if (!LLMAvailable()) {
    return GenerateFromTemplate(req);
  }

  auto t0 = std::chrono::steady_clock::now();
  CodeGenResult result;
  result.model_used = model_;

  std::string raw = QueryOllama(BuildSystemPrompt(), BuildUserPrompt(req));

  if (raw.empty()) {
    // LLM call failed — fall back
    result = GenerateFromTemplate(req);
    result.description =
        "(LLM unavailable — used template) " + result.description;
    return result;
  }

  ExtractCode(raw, result);

  if (result.code.empty()) {
    result = GenerateFromTemplate(req);
    result.description =
        "(LLM response unparseable — used template) " + result.description;
    return result;
  }

  if (result.cmake_snippet.empty() && req.include_cmake) {
    auto vars = DefaultVars(req);
    result.cmake_snippet = TemplateLibrary::Fill(
        CodeTemplate{"", "", {}, DEFAULT_CMAKE, ""}, vars);
  }

  result.description = req.description;
  result.success = true;

  auto t1 = std::chrono::steady_clock::now();
  result.elapsed =
      std::chrono::duration_cast<std::chrono::milliseconds>(t1 - t0);
  return result;
}

void CodeGenerator::GenerateAsync(
    const CodeGenRequest& req,
    std::function<void(const std::string& token)> on_token,
    std::function<void(CodeGenResult)> on_complete) {
  std::thread([req, on_token, on_complete, this]() mutable {
    // For simplicity, run synchronously in thread and fire tokens in chunks
    CodeGenResult r = Generate(req);
    if (on_token) {
      // Simulate streaming by breaking code into chunks of ~40 chars
      const std::size_t chunk_size = 40;
      for (std::size_t i = 0; i < r.code.size(); i += chunk_size) {
        on_token(r.code.substr(i, chunk_size));
      }
    }
    if (on_complete) {
      on_complete(std::move(r));
    }
  }).detach();
}

