ftxui::Component CodePreview(const std::string& code,
                             const std::string& language) {
  struct State {
    std::string code;
    std::string language;
    int scroll = 0;
    std::vector<Element> highlighted;

    void Rebuild() {
      highlighted = SyntaxHighlighter::Highlight(code, language);
    }
  };

  auto state = std::make_shared<State>();
  state->code = code;
  state->language = language;
  state->Rebuild();

  auto line_count = static_cast<int>(state->highlighted.size());

  class Impl : public ComponentBase {
   public:
    explicit Impl(std::shared_ptr<State> s) : state_(std::move(s)) {}

    Element OnRender() override {
      const int line_count = static_cast<int>(state_->highlighted.size());
      const int visible = 30;  // approx visible lines

      Elements lines;
      int start = std::max(0, std::min(state_->scroll, line_count - 1));
      int end = std::min(line_count, start + visible);

      for (int i = start; i < end; ++i) {
        std::string num = std::to_string(i + 1);
        while (num.size() < 4) {
          num = " " + num;
        }
        lines.push_back(hbox({
            text(num) | dim | size(WIDTH, EQUAL, 4),
            text(" │ ") | dim,
            state_->highlighted[i] | flex,
        }));
      }

      std::string header = " " + std::to_string(line_count) + " lines";

      return vbox({
                 hbox({
                     text(header) | bold,
                     filler(),
                 }) | color(Color::Cyan),
                 separator(),
                 vbox(std::move(lines)) | flex,
             }) |
             border;
    }

    bool OnEvent(Event event) override {
      int line_count = static_cast<int>(state_->highlighted.size());
      if (event == Event::ArrowUp || event == Event::Character('k')) {
        state_->scroll = std::max(0, state_->scroll - 1);
        return true;
      }
      if (event == Event::ArrowDown || event == Event::Character('j')) {
        state_->scroll = std::min(line_count - 1, state_->scroll + 1);
        return true;
      }
      if (event == Event::PageUp) {
        state_->scroll = std::max(0, state_->scroll - 20);
        return true;
      }
      if (event == Event::PageDown) {
        state_->scroll = std::min(line_count - 1, state_->scroll + 20);
        return true;
      }
      return false;
    }

   private:
    std::shared_ptr<State> state_;
  };

  (void)line_count;  // suppress unused warning when used in lambda captures
  return Make<Impl>(state);
}

