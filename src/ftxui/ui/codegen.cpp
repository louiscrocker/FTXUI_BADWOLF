const CodeTemplate* TemplateLibrary::FindBest(const std::string& description) {
  // Lower-case copy for case-insensitive matching
  std::string lower = description;
  std::transform(lower.begin(), lower.end(), lower.begin(), ::tolower);

  const CodeTemplate* best = nullptr;
  int best_score = 0;

  for (const auto& tmpl : BuiltinTemplates()) {
    int score = 0;
    for (const auto& kw : tmpl.keywords) {
      if (lower.find(kw) != std::string::npos) {
        ++score;
      }
    }
    if (score > best_score) {
      best_score = score;
      best = &tmpl;
    }
  }

  return (best_score >= 1) ? best : nullptr;
}

std::string TemplateLibrary::Fill(
    const CodeTemplate& tmpl,
    const std::map<std::string, std::string>& vars) {
  std::string result = tmpl.code;

  // Apply caller-provided substitutions
  for (const auto& [key, value] : vars) {
    std::string placeholder = "{" + key + "}";
    std::string::size_type pos = 0;
    while ((pos = result.find(placeholder, pos)) != std::string::npos) {
      result.replace(pos, placeholder.size(), value);
      pos += value.size();
    }
  }

  return result;
}

