ftxui::Component CodeGenStudio(
    std::function<void(const CodeGenResult&)> on_generated) {
  struct StudioState {
    std::string description;
    int style_index = 0;
    bool feat_charts = true;
    bool feat_tables = true;
    bool feat_realtime = false;
    bool feat_map = false;
    std::string model = "llama3";
    CodeGenResult result;
    bool generating = false;
    std::string status_msg;
    std::shared_ptr<Component> preview;
  };

  auto state = std::make_shared<StudioState>();

  // Description input
  auto desc_input = Input(&state->description, "kubernetes pod monitor");

  // Style dropdown
  std::vector<std::string> styles = {"modern", "minimal", "matrix", "lcars"};
  auto style_sel = Dropdown(&styles, &state->style_index);

  // Feature checkboxes
  auto feat_charts = Checkbox("Charts", &state->feat_charts);
  auto feat_tables = Checkbox("Tables", &state->feat_tables);
  auto feat_realtime = Checkbox("Real-time", &state->feat_realtime);
  auto feat_map = Checkbox("Map", &state->feat_map);

  // Generate button
  auto gen_btn = Button("Generate →", [state, on_generated]() {
    if (state->description.empty()) {
      return;
    }
    state->generating = true;
    state->status_msg = "Generating…";

    CodeGenRequest req;
    req.description = state->description;
    req.style = std::vector<std::string>{
        "modern", "minimal", "matrix",
        "lcars"}[static_cast<std::size_t>(state->style_index)];
    if (state->feat_charts) {
      req.features.push_back("charts");
    }
    if (state->feat_tables) {
      req.features.push_back("tables");
    }
    if (state->feat_realtime) {
      req.features.push_back("real-time");
    }
    if (state->feat_map) {
      req.features.push_back("map");
    }
    req.model = state->model;

    // Run in background thread
    std::thread([state, req, on_generated]() mutable {
      CodeGenerator gen(req.model);
      auto r = gen.Generate(req);
      state->result = r;
      state->generating = false;
      state->status_msg = r.success ? ("✓ " + r.description) : ("✗ " + r.error);
      if (on_generated && r.success) {
        on_generated(r);
      }
    }).detach();
  });

  // Template button
  auto tmpl_btn = Button("From Template", [state, on_generated]() {
    if (state->description.empty()) {
      return;
    }

    CodeGenRequest req;
    req.description = state->description;
    req.model = state->model;

    CodeGenerator gen(req.model);
    state->result = gen.GenerateFromTemplate(req);
    state->status_msg = state->result.success
                            ? ("✓ " + state->result.description)
                            : ("✗ " + state->result.error);
    if (on_generated && state->result.success) {
      on_generated(state->result);
    }
  });

  // Save button
  auto save_btn = Button("Save As…", [state]() {
    if (!state->result.success || state->result.code.empty()) {
      return;
    }
    std::string path = state->description;
    // Remove spaces, add .cpp
    std::transform(path.begin(), path.end(), path.begin(),
                   [](char c) { return c == ' ' ? '_' : c; });
    path += ".cpp";
    if (state->result.WriteToFile(path)) {
      state->status_msg = "✓ Saved to " + path;
    } else {
      state->status_msg = "✗ Failed to write " + path;
    }
  });

  auto container = Container::Horizontal({
      desc_input,
      style_sel,
      feat_charts,
      feat_tables,
      feat_realtime,
      feat_map,
      gen_btn,
      tmpl_btn,
      save_btn,
  });

  return Renderer(
      container,
      [state, container, desc_input, style_sel, feat_charts, feat_tables,
       feat_realtime, feat_map, gen_btn, tmpl_btn, save_btn]() -> Element {
        // Build preview of code
        Element preview_el = text("(no code generated yet)") | dim | hcenter;
        if (!state->result.code.empty()) {
          auto lines = SyntaxHighlighter::Highlight(state->result.code);
          int total = static_cast<int>(lines.size());
          // Show up to 20 lines
          Elements visible;
          visible.reserve(std::min(total, 20));
          for (int i = 0; i < std::min(total, 20); ++i) {
            std::string num = std::to_string(i + 1);
            while (num.size() < 3) {
              num = " " + num;
            }
            visible.push_back(hbox({
                text(num) | dim | size(WIDTH, EQUAL, 3),
                text(" │ ") | dim,
                lines[i] | flex,
            }));
          }
          if (total > 20) {
            visible.push_back(
                text("  … " + std::to_string(total - 20) + " more lines …") |
                dim);
          }

          std::string hdr =
              " Generated Code (" + std::to_string(total) + " lines)";
          preview_el = vbox({
              hbox({
                  text(hdr) | bold | color(Color::Cyan),
                  filler(),
                  save_btn->Render(),
              }),
              separator(),
              vbox(std::move(visible)),
          });
        }

        // Status bar
        Color status_color = (state->status_msg.rfind("✓", 0) == 0 ||
                              state->status_msg.rfind("\xe2\x9c\x93", 0) == 0)
                                 ? Color::Green
                                 : Color::Yellow;
        if (state->generating) {
          status_color = Color::Yellow;
        }

        return vbox({
                   // Title
                   text(" 🤖 BadWolf Code Generator") | bold |
                       color(Color::Cyan) | hcenter,
                   separator(),
                   // Controls row 1
                   hbox({
                       text("Describe your app: ") | bold,
                       desc_input->Render() | flex,
                   }) | border,
                   // Controls row 2
                   hbox({
                       text("Style: "),
                       style_sel->Render() | size(WIDTH, EQUAL, 12),
                       text("  Features: "),
                       feat_charts->Render(),
                       text(" "),
                       feat_tables->Render(),
                       text(" "),
                       feat_realtime->Render(),
                       text(" "),
                       feat_map->Render(),
                       filler(),
                       gen_btn->Render(),
                       text(" "),
                       tmpl_btn->Render(),
                   }),
                   separator(),
                   // Status
                   hbox({
                       text(state->generating ? "⏳ Generating…"
                                              : state->status_msg) |
                           color(status_color),
                       filler(),
                   }),
                   separator(),
                   // Code preview
                   preview_el | flex,
               }) |
               border;
      });
}

