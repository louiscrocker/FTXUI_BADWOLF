std::vector<SyntaxHighlighter::Token> SyntaxHighlighter::Tokenize(
    const std::string& line) {
  std::vector<Token> tokens;
  if (line.empty()) {
    return tokens;
  }

  // Preprocessor line: the whole line is one token
  if (!line.empty() && line.front() == '#') {
    tokens.push_back({line, TokenType::Preprocessor});
    return tokens;
  }

  std::size_t i = 0;
  const std::size_t n = line.size();

  while (i < n) {
    // Line comment
    if (i + 1 < n && line[i] == '/' && line[i + 1] == '/') {
      tokens.push_back({line.substr(i), TokenType::Comment});
      break;
    }

    // Block comment start
    if (i + 1 < n && line[i] == '/' && line[i + 1] == '*') {
      std::size_t end = line.find("*/", i + 2);
      std::size_t len = (end == std::string::npos) ? n - i : end - i + 2;
      tokens.push_back({line.substr(i, len), TokenType::Comment});
      i += len;
      continue;
    }

    // String literal "..."
    if (line[i] == '"') {
      std::size_t j = i + 1;
      while (j < n) {
        if (line[j] == '\\') {
          j += 2;
          continue;
        }
        if (line[j] == '"') {
          ++j;
          break;
        }
        ++j;
      }
      tokens.push_back({line.substr(i, j - i), TokenType::String});
      i = j;
      continue;
    }

    // Char literal '.'
    if (line[i] == '\'') {
      std::size_t j = i + 1;
      while (j < n) {
        if (line[j] == '\\') {
          j += 2;
          continue;
        }
        if (line[j] == '\'') {
          ++j;
          break;
        }
        ++j;
      }
      tokens.push_back({line.substr(i, j - i), TokenType::String});
      i = j;
      continue;
    }

    // Number
    if (std::isdigit(static_cast<unsigned char>(line[i])) ||
        (line[i] == '-' && i + 1 < n &&
         std::isdigit(static_cast<unsigned char>(line[i + 1])))) {
      std::size_t j = i;
      if (line[j] == '-') {
        ++j;
      }
      while (j < n && (std::isalnum(static_cast<unsigned char>(line[j])) ||
                       line[j] == '.' || line[j] == '_')) {
        ++j;
      }
      tokens.push_back({line.substr(i, j - i), TokenType::Number});
      i = j;
      continue;
    }

    // Identifier or keyword
    if (std::isalpha(static_cast<unsigned char>(line[i])) || line[i] == '_') {
      std::size_t j = i;
      while (j < n && (std::isalnum(static_cast<unsigned char>(line[j])) ||
                       line[j] == '_')) {
        ++j;
      }
      std::string word = line.substr(i, j - i);
      TokenType type = (CppKeywords().count(word) > 0) ? TokenType::Keyword
                                                       : TokenType::Normal;
      tokens.push_back({word, type});
      i = j;
      continue;
    }

    // Operators / punctuation: single char
    char c = line[i];
    bool is_op =
        (c == '+' || c == '-' || c == '*' || c == '/' || c == '=' || c == '<' ||
         c == '>' || c == '!' || c == '&' || c == '|' || c == '^' || c == '~' ||
         c == '%' || c == '?' || c == ':' || c == ';' || c == ',' || c == '.' ||
         c == '(' || c == ')' || c == '[' || c == ']' || c == '{' || c == '}');
    tokens.push_back(
        {std::string(1, c), is_op ? TokenType::Operator : TokenType::Normal});
    ++i;
  }

  return tokens;
}

ftxui::Element SyntaxHighlighter::HighlightLine(
    const std::string& line,
    const std::string& /*language*/) {
  auto tokens = Tokenize(line);
  if (tokens.empty()) {
    return text("");
  }

  Elements parts;
  parts.reserve(tokens.size());
  for (const auto& tok : tokens) {
    Element e = text(tok.text);
    switch (tok.type) {
      case TokenType::Keyword:
        e = e | bold | color(Color::Blue);
        break;
      case TokenType::String:
        e = e | color(Color::Green);
        break;
      case TokenType::Comment:
        e = e | dim | color(Color::GrayDark);
        break;
      case TokenType::Number:
        e = e | color(Color::Yellow);
        break;
      case TokenType::Preprocessor:
        e = e | color(Color::Magenta);
        break;
      case TokenType::Operator:
        e = e | color(Color::GrayLight);
        break;
      case TokenType::Normal:
      default:
        break;
    }
    parts.push_back(std::move(e));
  }
  return hbox(std::move(parts));
}

std::vector<ftxui::Element> SyntaxHighlighter::Highlight(
    const std::string& code,
    const std::string& language) {
  std::vector<Element> lines;
  std::istringstream ss(code);
  std::string line;
  while (std::getline(ss, line)) {
    lines.push_back(HighlightLine(line, language));
  }
  // If the code ends with a newline, add a trailing empty line
  if (!code.empty() && code.back() == '\n') {
    lines.push_back(HighlightLine("", language));
  }
  return lines;
}

